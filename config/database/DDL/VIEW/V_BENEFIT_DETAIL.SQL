CREATE OR REPLACE VIEW V_BENEFIT_DETAIL AS
SELECT
  -- 特典情報
  B.BENEFIT_ID,
  B.MUNICIPALITY_CD,
  B.BENEFIT_NAME,
  B.BENEFIT_SHORT_NAME,
  B.BENEFIT_DETAIL,
  B.EXP_DETAIL,
  B.PHONE_NUMBER,
  B.BENEFIT_URL,
  B.ADDRESS,
  B.LATITUDE,
  B.LONGITUDE,
  -- 自治体情報
  M.MUNICIPALITY_NAME,
  M.MUNICIPALITY_KANA,
  M.MUNICIPALITY_TYPE,
  -- カテゴリ情報
  BC.CATEGORY_CD,
  BC.CATEGORY_NAME,
  BC.DISPLAY_ORDER,
  BC.IS_ACTIVE         AS CATEGORY_IS_ACTIVE,
  -- 利用条件情報
  BE.ID                AS ELIGIBILITY_ID,
  BE.LICENSE_STATUS,
  BE.MIN_AGE,
  BE.MAX_AGE,
  BE.MUNICIPALITY_CD   AS ELIGIBILITY_MUNICIPALITY_CD,
  BE.NOTE              AS ELIGIBILITY_NOTE
FROM
  BENEFIT B
  INNER JOIN MUNICIPALITY M
    ON B.MUNICIPALITY_CD = M.MUNICIPALITY_CD
  INNER JOIN BENEFIT_CATEGORY BC
    ON B.CATEGORY_CD = BC.CATEGORY_CD
  LEFT JOIN BENEFIT_ELIGIBILITY BE
    ON B.BENEFIT_ID = BE.BENEFIT_ID;

COMMENT ON VIEW V_BENEFIT_DETAIL IS '特典詳細ビュー';
-- 特典情報
COMMENT ON COLUMN V_BENEFIT_DETAIL.BENEFIT_ID IS '特典ID';
COMMENT ON COLUMN V_BENEFIT_DETAIL.MUNICIPALITY_CD IS '自治体コード';
COMMENT ON COLUMN V_BENEFIT_DETAIL.BENEFIT_NAME IS '特典名称';
COMMENT ON COLUMN V_BENEFIT_DETAIL.BENEFIT_SHORT_NAME IS '特典短縮名称';
COMMENT ON COLUMN V_BENEFIT_DETAIL.BENEFIT_DETAIL IS '特典内容';
COMMENT ON COLUMN V_BENEFIT_DETAIL.EXP_DETAIL IS '有効期限';
COMMENT ON COLUMN V_BENEFIT_DETAIL.PHONE_NUMBER IS '問い合わせ電話番号';
COMMENT ON COLUMN V_BENEFIT_DETAIL.BENEFIT_URL IS '特典URL';
COMMENT ON COLUMN V_BENEFIT_DETAIL.ADDRESS IS '住所';
COMMENT ON COLUMN V_BENEFIT_DETAIL.LATITUDE IS '緯度';
COMMENT ON COLUMN V_BENEFIT_DETAIL.LONGITUDE IS '経度';
-- 自治体情報
COMMENT ON COLUMN V_BENEFIT_DETAIL.MUNICIPALITY_NAME IS '自治体名称';
COMMENT ON COLUMN V_BENEFIT_DETAIL.MUNICIPALITY_KANA IS '自治体名称かな';
COMMENT ON COLUMN V_BENEFIT_DETAIL.MUNICIPALITY_TYPE IS '自治体区分';
-- カテゴリ情報
COMMENT ON COLUMN V_BENEFIT_DETAIL.CATEGORY_CD IS '種別コード';
COMMENT ON COLUMN V_BENEFIT_DETAIL.CATEGORY_NAME IS '種別名称';
COMMENT ON COLUMN V_BENEFIT_DETAIL.DISPLAY_ORDER IS '表示順';
COMMENT ON COLUMN V_BENEFIT_DETAIL.CATEGORY_IS_ACTIVE IS '有効フラグ（1:有効,0:無効）';
-- 利用条件情報
COMMENT ON COLUMN V_BENEFIT_DETAIL.ELIGIBILITY_ID IS 'ID';
COMMENT ON COLUMN V_BENEFIT_DETAIL.LICENSE_STATUS IS '運転免許所持状況';
COMMENT ON COLUMN V_BENEFIT_DETAIL.MIN_AGE IS '最低年齢';
COMMENT ON COLUMN V_BENEFIT_DETAIL.MAX_AGE IS '最高年齢';
COMMENT ON COLUMN V_BENEFIT_DETAIL.ELIGIBILITY_MUNICIPALITY_CD IS '対象自治体コード';
COMMENT ON COLUMN V_BENEFIT_DETAIL.ELIGIBILITY_NOTE IS '備考';
