CREATE OR REPLACE VIEW V_FARE_DISCOUNT_ELIGIBILITY AS
SELECT
  -- 運賃割引情報
  FD.BENEFIT_ID,
  FD.AGENCY_ID,
  FD.DISCOUNT_TYPE,
  FD.DISCOUNT_VALUE,
  -- 利用条件情報
  BE.LICENSE_STATUS,
  BE.MIN_AGE,
  BE.MAX_AGE,
  BE.MUNICIPALITY_CD   AS ELIGIBILITY_MUNICIPALITY_CD
FROM
  FARE_DISCOUNT FD
  LEFT JOIN BENEFIT_ELIGIBILITY BE
    ON FD.BENEFIT_ID = BE.BENEFIT_ID;

COMMENT ON VIEW V_FARE_DISCOUNT_ELIGIBILITY IS '運賃割引条件ビュー';
-- 運賃割引情報
COMMENT ON COLUMN V_FARE_DISCOUNT_ELIGIBILITY.BENEFIT_ID IS '特典ID';
COMMENT ON COLUMN V_FARE_DISCOUNT_ELIGIBILITY.AGENCY_ID IS '事業者ID';
COMMENT ON COLUMN V_FARE_DISCOUNT_ELIGIBILITY.DISCOUNT_TYPE IS '割引種別';
COMMENT ON COLUMN V_FARE_DISCOUNT_ELIGIBILITY.DISCOUNT_VALUE IS '割引値';
-- 利用条件情報
COMMENT ON COLUMN V_FARE_DISCOUNT_ELIGIBILITY.LICENSE_STATUS IS '運転免許所持状況';
COMMENT ON COLUMN V_FARE_DISCOUNT_ELIGIBILITY.MIN_AGE IS '最低年齢';
COMMENT ON COLUMN V_FARE_DISCOUNT_ELIGIBILITY.MAX_AGE IS '最高年齢';
COMMENT ON COLUMN V_FARE_DISCOUNT_ELIGIBILITY.ELIGIBILITY_MUNICIPALITY_CD IS '対象自治体コード';
