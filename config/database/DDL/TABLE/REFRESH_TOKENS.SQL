CREATE TABLE REFRESH_TOKENS (
  TOKEN_ID       BIGSERIAL PRIMARY KEY,
  USER_ID        BIGINT NOT NULL,
  TOKEN_HASH     VARCHAR(64) NOT NULL,
  EXPIRES_AT     TIMESTAMP NOT NULL,
  REVOKED        BOOLEAN NOT NULL DEFAULT FALSE,
  SYS_CREATED_AT TIMESTAMP,
  SYS_UPDATED_AT TIMESTAMP,
  CONSTRAINT REFRESH_TOKENS_TOKEN_HASH_UNIQUE UNIQUE (TOKEN_HASH),
  CONSTRAINT REFRESH_TOKENS_FK FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID) ON DELETE CASCADE
);

CREATE INDEX REFRESH_TOKENS_USER_ID_IDX ON REFRESH_TOKENS (USER_ID);

COMMENT ON TABLE REFRESH_TOKENS IS 'リフレッシュトークンテーブル';
COMMENT ON COLUMN REFRESH_TOKENS.TOKEN_ID IS 'トークンID';
COMMENT ON COLUMN REFRESH_TOKENS.USER_ID IS 'ユーザーID';
COMMENT ON COLUMN REFRESH_TOKENS.TOKEN_HASH IS 'リフレッシュトークンのSHA-256ハッシュ値（64文字hex文字列。DBには平文を保存しない）';
COMMENT ON COLUMN REFRESH_TOKENS.EXPIRES_AT IS 'トークン有効期限';
COMMENT ON COLUMN REFRESH_TOKENS.REVOKED IS '失効フラグ（ローテーション・ログアウト時にtrue）';
COMMENT ON COLUMN REFRESH_TOKENS.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN REFRESH_TOKENS.SYS_UPDATED_AT IS '更新日時';
