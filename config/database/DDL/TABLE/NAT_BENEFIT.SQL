CREATE TABLE NAT_BENEFIT (
  BENEFIT_ID            VARCHAR(20) NOT NULL,
  MUNICIPALITY_CD       VARCHAR(6) NOT NULL,
  CATEGORY_CD           VARCHAR(20) NOT NULL,
  BENEFIT_NAME          VARCHAR(400),
  BENEFIT_SHORT_NAME    VARCHAR(200),
  BENEFIT_DETAIL        VARCHAR(1000),
  EXP_DETAIL            VARCHAR(200),
  PHONE_NUMBER          VARCHAR(50),
  BENEFIT_URL           VARCHAR(1000),
  SYS_CREATED_AT        TIMESTAMP,
  SYS_UPDATED_AT        TIMESTAMP,
  CONSTRAINT NAT_BENEFIT_PK PRIMARY KEY (BENEFIT_ID),
  CONSTRAINT NAT_BENEFIT_FK_MUNICIPALITY FOREIGN KEY (MUNICIPALITY_CD) REFERENCES NAT_MUNICIPALITY (MUNICIPALITY_CD),
  CONSTRAINT NAT_BENEFIT_FK_CATEGORY FOREIGN KEY (CATEGORY_CD) REFERENCES NAT_BENEFIT_CATEGORY (CATEGORY_CD)
);

COMMENT ON TABLE NAT_BENEFIT IS '運転免許返納特典テーブル';
COMMENT ON COLUMN NAT_BENEFIT.BENEFIT_ID IS '特典ID';
COMMENT ON COLUMN NAT_BENEFIT.MUNICIPALITY_CD IS '自治体コード';
COMMENT ON COLUMN NAT_BENEFIT.CATEGORY_CD IS 'カテゴリコード';
COMMENT ON COLUMN NAT_BENEFIT.BENEFIT_NAME IS '特典名称';
COMMENT ON COLUMN NAT_BENEFIT.BENEFIT_SHORT_NAME IS '特典短縮名称';
COMMENT ON COLUMN NAT_BENEFIT.BENEFIT_DETAIL IS '特典内容';
COMMENT ON COLUMN NAT_BENEFIT.EXP_DETAIL IS '有効期限';
COMMENT ON COLUMN NAT_BENEFIT.PHONE_NUMBER IS '問い合わせ電話番号';
COMMENT ON COLUMN NAT_BENEFIT.BENEFIT_URL IS '特典URL';
COMMENT ON COLUMN NAT_BENEFIT.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN NAT_BENEFIT.SYS_UPDATED_AT IS '更新日時';
