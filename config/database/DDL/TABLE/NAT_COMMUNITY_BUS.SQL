CREATE TABLE NAT_COMMUNITY_BUS (
  ROUTE_ID           VARCHAR(20) NOT NULL,
  COMMUNITY_BUS_ID   VARCHAR(20),
  ROUTE_NAME         VARCHAR(200),
  SYS_CREATED_AT     TIMESTAMP,
  SYS_UPDATED_AT     TIMESTAMP,
  CONSTRAINT NAT_COMMUNITY_BUS_PK PRIMARY KEY (ROUTE_ID),
  CONSTRAINT NAT_COMMUNITY_BUS_FK FOREIGN KEY (COMMUNITY_BUS_ID) REFERENCES NAT_AGENCY (AGENCY_ID)
);

COMMENT ON TABLE NAT_COMMUNITY_BUS IS 'コミュニティバス路線テーブル';
COMMENT ON COLUMN NAT_COMMUNITY_BUS.ROUTE_ID IS '路線ID';
COMMENT ON COLUMN NAT_COMMUNITY_BUS.COMMUNITY_BUS_ID IS 'コミュニティバスID';
COMMENT ON COLUMN NAT_COMMUNITY_BUS.ROUTE_NAME IS '行先名称';
COMMENT ON COLUMN NAT_COMMUNITY_BUS.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN NAT_COMMUNITY_BUS.SYS_UPDATED_AT IS '更新日時';