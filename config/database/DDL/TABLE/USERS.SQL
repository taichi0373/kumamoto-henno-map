CREATE TABLE USERS (
  USER_ID                BIGSERIAL PRIMARY KEY,
  USERNAME               VARCHAR(20) NOT NULL,
  PASSWORD_HASH          VARCHAR(200) NOT NULL,
  EMAIL                  VARCHAR(200),
  BIRTH_DATE             DATE,
  MUNICIPALITY_CODE      VARCHAR(6),
  LICENSE_STATUS         VARCHAR(1),
  LICENSE_SURRENDERED_AT DATE,
  SYS_CREATED_AT         TIMESTAMP,
  SYS_UPDATED_AT         TIMESTAMP,
  CONSTRAINT USERS_USERNAME_UNIQUE UNIQUE (USERNAME),
  CONSTRAINT USERS_FK FOREIGN KEY (MUNICIPALITY_CODE) REFERENCES MUNICIPALITY (MUNICIPALITY_CD)
);

COMMENT ON TABLE USERS IS 'ユーザーテーブル';
COMMENT ON COLUMN USERS.USER_ID IS 'ユーザーID';
COMMENT ON COLUMN USERS.USERNAME IS 'ユーザー名';
COMMENT ON COLUMN USERS.PASSWORD_HASH IS 'パスワード';
COMMENT ON COLUMN USERS.EMAIL IS 'メールアドレス';
COMMENT ON COLUMN USERS.BIRTH_DATE IS '生年月日';
COMMENT ON COLUMN USERS.MUNICIPALITY_CODE IS '自治体コード';
COMMENT ON COLUMN USERS.LICENSE_STATUS IS '運転免許所持状況';
COMMENT ON COLUMN USERS.LICENSE_SURRENDERED_AT IS '運転免許返納日';
COMMENT ON COLUMN USERS.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN USERS.SYS_UPDATED_AT IS '更新日時';
