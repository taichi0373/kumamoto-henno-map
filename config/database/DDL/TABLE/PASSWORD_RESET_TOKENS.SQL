CREATE TABLE PASSWORD_RESET_TOKENS (
  TOKEN_ID       BIGSERIAL PRIMARY KEY,
  USER_ID        BIGINT NOT NULL,
  TOKEN          VARCHAR(64) NOT NULL,
  EXPIRES_AT     TIMESTAMP NOT NULL,
  USED           BOOLEAN NOT NULL DEFAULT FALSE,
  SYS_CREATED_AT TIMESTAMP,
  SYS_UPDATED_AT TIMESTAMP,
  CONSTRAINT PASSWORD_RESET_TOKENS_TOKEN_UNIQUE UNIQUE (TOKEN),
  CONSTRAINT PASSWORD_RESET_TOKENS_FK FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID)
);

COMMENT ON TABLE PASSWORD_RESET_TOKENS IS 'パスワードリセットトークンテーブル';
COMMENT ON COLUMN PASSWORD_RESET_TOKENS.TOKEN_ID IS 'トークンID';
COMMENT ON COLUMN PASSWORD_RESET_TOKENS.USER_ID IS 'ユーザーID';
COMMENT ON COLUMN PASSWORD_RESET_TOKENS.TOKEN IS 'リセットトークンのSHA-256ハッシュ値（64文字hex文字列）';
COMMENT ON COLUMN PASSWORD_RESET_TOKENS.EXPIRES_AT IS 'トークン有効期限（発行から30分）';
COMMENT ON COLUMN PASSWORD_RESET_TOKENS.USED IS '使用済みフラグ';
COMMENT ON COLUMN PASSWORD_RESET_TOKENS.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN PASSWORD_RESET_TOKENS.SYS_UPDATED_AT IS '更新日時';
