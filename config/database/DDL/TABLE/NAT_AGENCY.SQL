CREATE TABLE NAT_AGENCY (
  AGENCY_ID        VARCHAR(20) NOT NULL,
  AGENCY_NAME      VARCHAR(100) NOT NULL,
  AGENCY_KANA      VARCHAR(200),
  PHONE_NUMBER     VARCHAR(20),
  AGENCY_URL       VARCHAR(1000),
  OPERATOR_ID      VARCHAR(20),
  SYS_CREATED_AT   TIMESTAMP,
  SYS_UPDATED_AT   TIMESTAMP,
  CONSTRAINT NAT_AGENCY_PK PRIMARY KEY (AGENCY_ID)
);

COMMENT ON TABLE NAT_AGENCY IS '事業者マスタテーブル';
COMMENT ON COLUMN NAT_AGENCY.AGENCY_ID IS '事業者ID';
COMMENT ON COLUMN NAT_AGENCY.AGENCY_NAME IS '事業者名称';
COMMENT ON COLUMN NAT_AGENCY.AGENCY_KANA IS '事業者名称かな';
COMMENT ON COLUMN NAT_AGENCY.PHONE_NUMBER IS '問い合わせ電話番号';
COMMENT ON COLUMN NAT_AGENCY.AGENCY_URL IS '事業者URL';
COMMENT ON COLUMN NAT_AGENCY.OPERATOR_ID IS '運行事業者ID';
COMMENT ON COLUMN NAT_AGENCY.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN NAT_AGENCY.SYS_UPDATED_AT IS '更新日時';
