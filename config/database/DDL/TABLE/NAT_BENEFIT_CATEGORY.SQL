CREATE TABLE NAT_BENEFIT_CATEGORY (
  CATEGORY_CD           VARCHAR(20) NOT NULL,
  CATEGORY_NAME         VARCHAR(200) NOT NULL,
  DISPLAY_ORDER         INTEGER,
  IS_ACTIVE             CHAR(1) DEFAULT '1' NOT NULL,
  SYS_CREATED_AT        TIMESTAMP,
  SYS_UPDATED_AT        TIMESTAMP,
  CONSTRAINT NAT_BENEFIT_CATEGORY_PK PRIMARY KEY (CATEGORY_CD)
);

COMMENT ON TABLE NAT_BENEFIT_CATEGORY IS '特典種別マスタテーブル';
COMMENT ON COLUMN NAT_BENEFIT_CATEGORY.CATEGORY_CD IS '種別コード';
COMMENT ON COLUMN NAT_BENEFIT_CATEGORY.CATEGORY_NAME IS '種別名称';
COMMENT ON COLUMN NAT_BENEFIT_CATEGORY.DISPLAY_ORDER IS '表示順';
COMMENT ON COLUMN NAT_BENEFIT_CATEGORY.IS_ACTIVE IS '有効フラグ（1:有効,0:無効）';
COMMENT ON COLUMN NAT_BENEFIT_CATEGORY.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN NAT_BENEFIT_CATEGORY.SYS_UPDATED_AT IS '更新日時';