CREATE TABLE FARE_DISCOUNT (
  BENEFIT_ID         VARCHAR(20) NOT NULL,
  AGENCY_ID          VARCHAR(20) NOT NULL, 
  DISCOUNT_TYPE      VARCHAR(1),  -- 0: percentage, 1: free
  DISCOUNT_VALUE     NUMERIC(10, 2),
  SYS_CREATED_AT     TIMESTAMP,
  SYS_UPDATED_AT     TIMESTAMP,
  CONSTRAINT FARE_DISCOUNT_PK PRIMARY KEY (BENEFIT_ID, AGENCY_ID),
  CONSTRAINT FARE_DISCOUNT_BENEFIT_FK FOREIGN KEY (BENEFIT_ID) REFERENCES BENEFIT (BENEFIT_ID),
  CONSTRAINT FARE_DISCOUNT_AGENCY_FK FOREIGN KEY (AGENCY_ID) REFERENCES AGENCY (AGENCY_ID)
);

COMMENT ON TABLE FARE_DISCOUNT IS '運転免許返納特典運賃割引テーブル';
COMMENT ON COLUMN FARE_DISCOUNT.BENEFIT_ID IS '特典ID';
COMMENT ON COLUMN FARE_DISCOUNT.AGENCY_ID IS '事業者ID';
COMMENT ON COLUMN FARE_DISCOUNT.DISCOUNT_TYPE IS '割引種別';
COMMENT ON COLUMN FARE_DISCOUNT.DISCOUNT_VALUE IS '割引値';
COMMENT ON COLUMN FARE_DISCOUNT.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN FARE_DISCOUNT.SYS_UPDATED_AT IS '更新日時';
