CREATE TABLE MUNICIPALITY (
  MUNICIPALITY_CD     VARCHAR(6) NOT NULL,
  MUNICIPALITY_NAME   VARCHAR(200) NOT NULL,
  MUNICIPALITY_KANA   VARCHAR(200),
  SYS_CREATED_AT      TIMESTAMP,
  SYS_UPDATED_AT      TIMESTAMP,
  CONSTRAINT MUNICIPALITY_PK PRIMARY KEY (MUNICIPALITY_CD)
);

COMMENT ON TABLE MUNICIPALITY IS '自治体マスタテーブル';
COMMENT ON COLUMN MUNICIPALITY.MUNICIPALITY_CD IS '自治体コード';
COMMENT ON COLUMN MUNICIPALITY.MUNICIPALITY_NAME IS '自治体名称';
COMMENT ON COLUMN MUNICIPALITY.MUNICIPALITY_KANA IS '自治体名称かな';
COMMENT ON COLUMN MUNICIPALITY.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN MUNICIPALITY.SYS_UPDATED_AT IS '更新日時';
