CREATE TABLE BENEFIT_ELIGIBILITY (
  ID                              BIGSERIAL PRIMARY KEY,
  BENEFIT_ID                      VARCHAR(20) NOT NULL,
  LICENSE_STATUS                  VARCHAR(1),
  MIN_AGE                         INTEGER,
  MAX_AGE                         INTEGER,
  MUNICIPALITY_CD                 VARCHAR(6),
  NOTE                            VARCHAR(1000),
  SYS_CREATED_AT                  TIMESTAMP,
  SYS_UPDATED_AT                  TIMESTAMP,
  CONSTRAINT BENEFIT_ELIGIBILITY_FK FOREIGN KEY (BENEFIT_ID) REFERENCES BENEFIT (BENEFIT_ID)
);

COMMENT ON TABLE BENEFIT_ELIGIBILITY IS '運転免許返納特典条件テーブル';
COMMENT ON COLUMN BENEFIT_ELIGIBILITY.ID IS 'ID';
COMMENT ON COLUMN BENEFIT_ELIGIBILITY.BENEFIT_ID IS '特典ID';
COMMENT ON COLUMN BENEFIT_ELIGIBILITY.LICENSE_STATUS IS '運転免許所持状況';
COMMENT ON COLUMN BENEFIT_ELIGIBILITY.MIN_AGE IS '最低年齢';
COMMENT ON COLUMN BENEFIT_ELIGIBILITY.MAX_AGE IS '最高年齢';
COMMENT ON COLUMN BENEFIT_ELIGIBILITY.MUNICIPALITY_CD IS '対象自治体コード';
COMMENT ON COLUMN BENEFIT_ELIGIBILITY.NOTE IS '備考';
COMMENT ON COLUMN BENEFIT_ELIGIBILITY.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN BENEFIT_ELIGIBILITY.SYS_UPDATED_AT IS '更新日時';
