CREATE TABLE NAT_USER (
  USER_ID                VARCHAR(10) NOT NULL,
  USERNAME               VARCHAR(20) NOT NULL,
  PASSWORD_HASH          VARCHAR(200) NOT NULL,
  EMAIL                  VARCHAR(200),
  BIRTH_DATE             DATE,
  MUNICIPALITY_CODE      VARCHAR(6),
  LICENSE_STATUS         VARCHAR(1), -- 0:未所持, 1:所持, 2:返納, 3:失効, 4:その他
  LICENSE_SURRENDERED_AT DATE,
  SYS_CREATED_AT         TIMESTAMP,
  SYS_UPDATED_AT         TIMESTAMP,
  CONSTRAINT NAT_USER_PK PRIMARY KEY (USER_ID),
  CONSTRAINT NAT_USER_FK FOREIGN KEY (MUNICIPALITY_CODE) REFERENCES NAT_MUNICIPALITY (MUNICIPALITY_CD)
);

COMMENT ON TABLE NAT_USER IS 'ユーザーテーブル';
COMMENT ON COLUMN NAT_USER.USER_ID IS 'ユーザーID';
COMMENT ON COLUMN NAT_USER.USERNAME IS 'ユーザー名';
COMMENT ON COLUMN NAT_USER.PASSWORD_HASH IS 'パスワード';
COMMENT ON COLUMN NAT_USER.EMAIL IS 'メールアドレス';
COMMENT ON COLUMN NAT_USER.BIRTH_DATE IS '生年月日';
COMMENT ON COLUMN NAT_USER.MUNICIPALITY_CODE IS '自治体コード';
COMMENT ON COLUMN NAT_USER.LICENSE_STATUS IS '運転免許所持状況';
COMMENT ON COLUMN NAT_USER.LICENSE_SURRENDERED_AT IS '運転免許返納日';
COMMENT ON COLUMN NAT_USER.SYS_CREATED_AT IS '作成日時';
COMMENT ON COLUMN NAT_USER.SYS_UPDATED_AT IS '更新日時';